CREATE TABLE IF NOT EXISTS "orders" (
	"id" INTEGER NOT NULL,
	PRIMARY KEY("id")
);

CREATE TABLE IF NOT EXISTS "items" (
	"id" INTEGER NOT NULL,
	"order_id" INTEGER NOT NULL,
	PRIMARY KEY("id")
);

ALTER TABLE "items"
ADD FOREIGN KEY("order_id") REFERENCES "orders"("id")
ON UPDATE NO ACTION ON DELETE NO ACTION;
